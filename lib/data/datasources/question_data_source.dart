  List<Question> getQuestions(String category, String difficulty, int limit) {
    List<Question> filtered = _allQuestions
        .where(
          (q) =>
              q.category.toLowerCase() == category.toLowerCase() &&
              q.difficulty.toLowerCase() == difficulty.toLowerCase(),
        )
        .toList();

    filtered.shuffle(Random());
    return filtered.take(limit).toList();
  }

  List<Question> getQuestionsByCategory(String category, int limit) {
    List<Question> filtered = _allQuestions
        .where((q) => q.category.toLowerCase() == category.toLowerCase())
        .toList();

    filtered.shuffle(Random());
    return filtered.take(limit).toList();
  }

  List<Question> getQuestionsByDifficulty(String difficulty) {
    return _allQuestions
        .where((q) => q.difficulty.toLowerCase() == difficulty.toLowerCase())
        .toList();
  }

  List<Question> getRandomQuestions(int limit) {
    List<Question> shuffled = List.from(_allQuestions)..shuffle(Random());
    return shuffled.take(limit).toList();
  }
